use lpn_estimator::{LpnParams, LpnType};
use quote::quote;
use syn::File;

fn main() {
    eprintln!(
        "\nThis program searches primal LPN instances over F2 satisfying a minimum bit security for a given secret length."
    );
    eprintln!(
        "Output is formatted rust code written to STDOUT, which can be included as a module in other projects."
    );
    eprintln!("\nParameters: type, s, t, max_n (optional)");
    eprintln!("\ttype - either \"exact\" or \"regular\"");
    eprintln!("\ts - minimum bit security");
    eprintln!("\tt - hamming weight of the error");
    eprintln!("\tmax_n (optional) - maximum number of samples to search for\n");

    let mut args = std::env::args().skip(1);
    if args.len() > 4 || args.len() < 3 {
        panic!("You need to provide 3 or 4 parameters for the LPN estimator.")
    }

    let typ = match args.next().unwrap().as_str() {
        "exact" => LpnType::Exact,
        "regular" => LpnType::Regular,
        _ => panic!("Unable to parse LPN type"),
    };

    let s: f64 = args
        .next()
        .unwrap()
        .parse()
        .expect("Unable to parse number");

    let t: u64 = args
        .next()
        .unwrap()
        .parse()
        .expect("Unable to parse number");

    let max_n = args
        .next()
        .map(|n| n.parse().expect("Unable to parse number"));

    let lpns = LpnParams::scan(typ, s, t, max_n);

    let lpn_params_module = quote! {
        //! Do NOT edit this file manually!
        //!
        //! This module is automatically generated with the crate `lpn-estimator` using the binary
        //! `estimator.rs`. If you want to make changes run the estimator and replace this file
        //! with the generated output.

        use mpz_core::lpn::LpnParameters;

        /// LPN parameters.
        pub static LPN_PARAMS: &[LpnParameters] = &[#(#lpns),*];
    };

    let lpn_params_code: File = syn::parse2(lpn_params_module).unwrap();
    let pretty_code = prettyplease::unparse(&lpn_params_code);

    println!("{}", pretty_code);
}
